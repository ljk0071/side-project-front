메이플 파티 개인정보처리방침
메이플 파티(이하 '서비스')는 「개인정보 보호법」 제30조에 따라 정보주체의 개인정보를 보호하고 이와 관련한 고충을 신속하고 원활하게 처리할 수 있도록 하기 위하여 다음과 같이 개인정보 처리방침을 수립·공개합니다.

제1조 (개인정보의 처리 목적)

서비스는 다음의 목적을 위하여 개인정보를 처리합니다. 처리하고 있는 개인정보는 다음의 목적 이외의 용도로는 이용되지 않으며, 이용 목적이 변경되는 경우에는 「개인정보 보호법」 제18조에 따라 별도의 동의를 받는 등 필요한 조치를 이행할 예정입니다.

회원 가입 및 관리
회원 식별, 회원자격 유지·관리, 서비스 부정이용 방지, 만 14세 미만 아동의 개인정보 처리 시 법정대리인의 동의 여부 확인, 각종 고지·통지, 고충처리 등을 목적으로 개인정보를 처리합니다.
서비스 제공 및 운영
실시간 채팅 서비스 제공, 콘텐츠 제공, 본인인증 등 서비스 제공에 관련한 목적으로 개인정보를 처리합니다.
문의 및 고충 처리
이용자의 신원 확인, 문의사항 확인, 사실조사를 위한 연락·통지, 처리결과 통보 등의 목적으로 개인정보를 처리합니다.
제2조 (처리하는 개인정보의 항목 및 수집 방법)

① 서비스는 다음의 개인정보 항목을 처리하고 있습니다.

필수 항목: 이메일 주소, 비밀번호, 닉네임
서비스 이용 과정에서 자동 생성되는 정보: IP주소, 쿠키, 서비스 이용 기록, 기기정보
② 서비스는 다음과 같은 방법으로 개인정보를 수집합니다.

회원가입 및 서비스 이용 과정에서 이용자가 개인정보 수집에 대해 동의를 하고 직접 정보를 입력하는 경우
고객센터를 통한 상담 과정에서 웹페이지, 이메일 등을 통해 수집되는 경우
제3조 (개인정보의 처리 및 보유 기간)

① 서비스는 법령에 따른 개인정보 보유·이용기간 또는 정보주체로부터 개인정보를 수집 시에 동의받은 개인정보 보유·이용기간 내에서 개인정보를 처리·보유합니다.

② 각각의 개인정보 처리 및 보유 기간은 다음과 같습니다.

회원 가입 및 관리: 회원 탈퇴 시까지. 다만, 다음의 사유에 해당하는 경우에는 해당 사유 종료 시까지 보유합니다.
관계 법령 위반에 따른 수사·조사 등이 진행 중인 경우에는 해당 수사·조사 종료 시까지
서비스 이용에 따른 채권·채무관계 잔존 시에는 해당 정산 시까지
부정 이용 방지를 위해 탈퇴 후 6개월간 보관
전자상거래 등에서의 소비자 보호에 관한 법률
계약 또는 청약철회 등에 관한 기록: 5년
대금결제 및 재화 등의 공급에 관한 기록: 5년
소비자의 불만 또는 분쟁처리에 관한 기록: 3년
통신비밀보호법
로그인 기록: 3개월
제4조 (개인정보의 제3자 제공에 관한 사항)

서비스는 정보주체의 개인정보를 제1조(개인정보의 처리 목적)에서 명시한 범위 내에서만 처리하며, 정보주체의 동의, 법률의 특별한 규정 등 「개인정보 보호법」 제17조 및 제18조에 해당하는 경우에만 개인정보를 제3자에게 제공합니다. 현재 서비스는 개인정보를 제3자에게 제공하고 있지 않습니다.

제5조 (개인정보처리의 위탁에 관한 사항)

서비스는 원활한 개인정보 업무처리를 위하여 다음과 같이 개인정보 처리업무를 위탁할 수 있습니다. 위탁 계약 시 개인정보가 안전하게 관리될 수 있도록 필요한 사항을 규정하고 있으며, 위탁업무의 내용이나 수탁자가 변경될 경우에는 지체없이 본 개인정보처리방침을 통하여 공개하도록 하겠습니다.

현재 서비스는 개인정보 처리업무를 외부에 위탁하고 있지 않습니다.

제6조 (정보주체와 법정대리인의 권리·의무 및 그 행사방법)

① 정보주체는 서비스에 대해 언제든지 개인정보 열람·정정·삭제·처리정지 요구 등의 권리를 행사할 수 있습니다.

② 권리 행사는 서비스 내 ‘내 정보 관리’ 기능을 통하시거나, 제9조에 기재된 개인정보 보호책임자에게 이메일로 연락하시면 지체없이 조치하겠습니다.

③ 정보주체가 개인정보의 오류 등에 대한 정정 또는 삭제를 요구한 경우에는 정정 또는 삭제를 완료할 때까지 당해 개인정보를 이용하거나 제공하지 않습니다.

④ 만 14세 미만 아동의 경우, 법정대리인이 아동의 개인정보에 대한 권리를 행사할 수 있습니다.

제7조 (개인정보의 파기절차 및 파기방법)

① 서비스는 개인정보 보유기간의 경과, 처리목적 달성 등 개인정보가 불필요하게 되었을 때에는 지체없이 해당 개인정보를 파기합니다.

② 개인정보 파기의 절차 및 방법은 다음과 같습니다.

파기절차: 파기 사유가 발생한 개인정보를 선정하고, 개인정보 보호책임자의 승인을 받아 개인정보를 파기합니다.
파기방법: 전자적 파일 형태로 기록·저장된 개인정보는 기록을 재생할 수 없는 기술적 방법을 사용하여 삭제하며, 종이 문서에 기록·저장된 개인정보는 분쇄기로 분쇄하거나 소각하여 파기합니다.
제8조 (개인정보의 안전성 확보조치)

서비스는 개인정보의 안전성 확보를 위해 다음과 같은 조치를 취하고 있습니다.

관리적 조치: 내부관리계획 수립·시행, 개인정보 취급자에 대한 정기적인 교육
기술적 조치: 개인정보처리시스템 등의 접근권한 관리, 접근통제시스템 설치, 고유식별정보 등의 암호화, 보안프로그램 설치
제9조 (개인정보 보호책임자)

서비스는 개인정보 처리에 관한 업무를 총괄해서 책임지고, 개인정보 처리와 관련한 정보주체의 불만처리 및 피해구제 등을 위하여 아래와 같이 개인정보 보호책임자를 지정하고 있습니다.

